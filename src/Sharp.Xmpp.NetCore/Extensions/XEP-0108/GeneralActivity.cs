namespace Sharp.Xmpp.Extensions
{
    /// <summary>
    /// Defines possible values for the general activity of an XMPP user.
    /// </summary>
    /// <remarks>Refer to XEP-0108 for a detailed taxonomy and description of the
    /// various activity values.</remarks>
    public enum GeneralActivity
    {
        /// <summary>
        /// Doing chores.
        /// </summary>
        DoingChores,

        /// <summary>
        /// Drinking.
        /// </summary>
        Drinking,

        /// <summary>
        /// Eating.
        /// </summary>
        Eating,

        /// <summary>
        /// Exercising (Cycling, Jogging, Swimming, etc.).
        /// </summary>
        Exercising,

        /// <summary>
        /// Grooming.
        /// </summary>
        Grooming,

        /// <summary>
        /// Having an appointment.
        /// </summary>
        HavingAppointment,

        /// <summary>
        /// Currently inactive.
        /// </summary>
        Inactive,

        /// <summary>
        /// Relaxing.
        /// </summary>
        Relaxing,

        /// <summary>
        /// Talking to somebody.
        /// </summary>
        Talking,

        /// <summary>
        /// Traveling.
        /// </summary>
        Traveling,

        /// <summary>
        /// Working.
        /// </summary>
        Working,

        /// <summary>
        /// Undefined activity.
        /// </summary>
        Undefined
    }
}